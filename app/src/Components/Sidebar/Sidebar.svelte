    import {location} from 'svelte-spa-router';

    // Action to update nav icon if active
    const adaptiveIcon = (node, item) => {
        function check() {
            if(item.descending !== true && $location === item.route) {
                node.innerHTML = item.iconActive;
            }
            else if(item.descending === true && $location.startsWith(item.route) === true) {
                node.innerHTML = item.iconActive;
            }
            else {
                node.innerHTML = item.icon;
            }
        }

        check();

        return {
            update(item) {
                check();
            },
            destroy() {}
        };
    };

